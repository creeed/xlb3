#include "XlbeException.h"
#include "XlbeNode.h"
#include "XlbeActionInstant.h"

/////////////////////////////////////////////////////////////////////////////////////////////////
namespace xlbe {

/////////////////////////////////////////////////////////////////////////////////////////////////
void Action_Show::start_with_target(Node *target)
{
    __super::start_with_target(target);
    target->set_visible(true);
}

/////////////////////////////////////////////////////////////////////////////////////////////////
void Action_Hide::start_with_target(Node *target)
{
    __super::start_with_target(target);
    target->set_visible(false);
}

/////////////////////////////////////////////////////////////////////////////////////////////////
void Action_Toggle_Visibility::start_with_target(Node *target)
{
    __super::start_with_target(target);
    target->set_visible(!target->is_visible());
}

/////////////////////////////////////////////////////////////////////////////////////////////////
void Action_Place::start_with_target(Node *target)
{
    __super::start_with_target(target);
    target->set_position(position_);
}

/////////////////////////////////////////////////////////////////////////////////////////////////
void Action_Func::start_with_target(Node *target)
{
    __super::start_with_target(target);

    (*slot_func_)(target, data_);
}

/////////////////////////////////////////////////////////////////////////////////////////////////
}